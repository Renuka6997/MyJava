class Student
{
	String name;
	int id;
	double tenth;
	double inter;
	double degree;
	double master;
	double phd;
	public Student(String name, int id, double tenth, double inter, double degree)
	{
		this.name=name;
		this.id=id;
		this.tenth=tenth;
		this.inter=inter;
		this.degree=degree;
	}
	public Student(String name, int id, double tenth, double inter, double degree, double master)
	{
		this.name=name;
		this.id=id;
		this.tenth=tenth;
		this.inter=inter;
		this.degree=degree;
		this.master=master;
	}
	public Student(String name, int id, double tenth, double inter, double degree, double master, double phd)
	{
		this.name=name;
		this.id=id;
		this.tenth=tenth;
		this.inter=inter;
		this.degree=degree;
		this.master=master;
		this.phd=phd;
	}
	public void studentinfo()
	{
		System.out.println("Name:"+this.name);
		System.out.println("id:"+this.id);
		System.out.println("tenth:"+this.tenth);
		System.out.println("inter:"+this.inter);
		System.out.println("Degree:"+this.degree);
		System.out.println("Master:"+this.master);
		System.out.println("phd:"+this.phd);
	}
}
